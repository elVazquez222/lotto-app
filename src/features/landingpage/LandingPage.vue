      requestAnimationFrame(animate);

      ball_16.rotation.x += 0.005;
      ball_5.rotation.x += 0.005;
      ball_32.rotation.x += 0.005;

      renderer.render(scene, camera);
      renderer.render(scene, camera);
    }

    animate();

    return { };
  },
  components: {},
};
